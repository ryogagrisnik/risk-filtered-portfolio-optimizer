"""
Portfolio optimization with covariance-based risk modeling and 
iterative factor-concentration filtering.

Pipeline:
1. Load daily returns from returns.csv
2. Compute covariance matrix, factor analysis (correlations + betas to ETFs)
3. Historical-mean expected returns
4. Sharpe-maximization optimizer (baseline)
5. Iterative risk filter that detects multi-stock factor concentration
   and applies dynamic group-weight caps via constraint generation
6. Multi-window out-of-sample backtest comparing filtered vs unfiltered vs benchmarks

Usage: python calc.py
Requires: returns.csv in the working directory (generated by download.py)
"""


