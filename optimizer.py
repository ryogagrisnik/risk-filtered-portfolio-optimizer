#  MULTIPLE OUT-OF-SAMPLE BACKTESTS 
# Run the same backtest across multiple train/test splits.
# This tests whether the filter's outperformance is consistent or regime-dependent.

split_dates = ['2023-01-01', '2023-07-01', '2024-01-01', '2024-07-01']

results = []

for split_date in split_dates:
    train_data = data[data.index < split_date]
    test_data = data[data.index >= split_date]
    
    # Skip if not enough train or test data
    if len(train_data) < 252 or len(test_data) < 60:
        continue
    
    # Re-optimize on training window
    train_hist_returns = (train_data[stocks].mean() * 252).values
    train_cov = (train_data[stocks].cov() * 252).values
    bs = [(0, 0.15) for _ in range(len(stocks))]
    
    # Unfiltered
    res_u = minimize(
        calc_sharpe_ratio, initial_weights,
        args=(train_cov, train_hist_returns, rf),
        method='SLSQP', bounds=bs,
        constraints=[{'type': 'eq', 'fun': lambda w: np.sum(w) - 1}]
    )
    unf_weights = pd.Series(res_u.x, index=stocks)
    
    # Filtered
    fil_weights = optimize_w_risk_filter(train_data)
    
    # Apply to test data
    r_unf = test_data[stocks] @ unf_weights.values
    r_fil = test_data[stocks] @ fil_weights.values
    r_spy = test_data['SPY']
    r_1n = test_data[stocks].mean(axis=1)
    
    def sharpe(daily, rf_annual=0.045):
        ann_ret = daily.mean() * 252
        ann_vol = daily.std() * np.sqrt(252)
        return (ann_ret - rf_annual) / ann_vol
    
    results.append({
        'split': split_date,
        'test_days': len(test_data),
        'unfiltered': round(sharpe(r_unf), 3),
        'filtered': round(sharpe(r_fil), 3),
        'spy': round(sharpe(r_spy), 3),
        '1/N': round(sharpe(r_1n), 3),
        'filter_advantage': round(sharpe(r_fil) - sharpe(r_unf), 3)
    })

# Print results as a table
