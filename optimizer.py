# get expected return values --> using historical mean annual returns
def get_hist_returns(returns_data):
    expected_return = returns_data[stocks].mean() * 252
    return expected_return


# calculate the sharpe ratio, and return it as negative for scipy minimization (cannot max)
def calc_sharpe_ratio(weights, covariance_matrix, expected_returns, rf_rate):
    portfolio_return = np.dot(expected_returns, weights)
    portfolio_std = np.sqrt(np.dot(np.dot(weights, covariance_matrix), weights))
    sharpe = (portfolio_return - rf_rate) / portfolio_std
    return 0 - sharpe


# Multivariate Risk Filter --> stocks have high corr with each other, both share same dominant ETF, high ETF corr. --> caps at 40% total combined portfolio weight
def multi_risk_filter(corr_grid, corr_matrix, optimal_weights):

    flagged_pairs = []
    stock_etf_corr_thresh = 0.70
    combined_weight_thresh = 0.40
    inter_stock_corr_thresh = 0.60

    for etf in corr_grid.columns:
        problems_series = corr_grid[etf] >= stock_etf_corr_thresh
        strong_corr_stocks = corr_grid.index[problems_series].to_list()
        
        if not strong_corr_stocks:
            continue 
        
        if len(strong_corr_stocks) < 2:
            continue
            
        # check corr between each other 
        sub_corr = corr_matrix.loc[strong_corr_stocks, strong_corr_stocks]
        confirmed_stocks = []
        
        for stock in sub_corr.columns:
            problem_stocks = sub_corr[stock] >= inter_stock_corr_thresh
            within_stock_str_corr_stocks = sub_corr.index[problem_stocks].to_list()

            if stock in within_stock_str_corr_stocks:
                within_stock_str_corr_stocks.remove(stock)
            
            if len(within_stock_str_corr_stocks) >= 1:
                confirmed_stocks.append(stock)
        
        confirmed_weight = optimal_weights[confirmed_stocks].sum()
        if confirmed_weight > combined_weight_thresh:
            flagged_pairs.append({
                'etf': etf,
                'correlated_stocks': confirmed_stocks,
                'combined_weight': round(confirmed_weight, 3)
            })
    return pd.DataFrame(flagged_pairs)
        

def optimize_w_risk_filter(train_data, loop_number=5):    
    # Iteratively optimize Sharpe, then check for factor concentration with the filter.
    # If flagged, add group-weight constraints and re-optimize. Repeat until clean. 
    bounds = [(0, 0.15) for _ in range(len(stocks))] 
    combined_weight_thresh = 0.40
    hist_returns_array = (train_data[stocks].mean() * 252).values
    cov_matrix_stocks_array = (train_data[stocks].cov() * 252).values

    # recompute corr_grid and corr_matrix for THIS training window
    train_corr_grid = pd.DataFrame(index=stocks, columns=etfs, dtype=float)
    for stock in stocks:
        for etf in etfs:
            train_corr_grid.loc[stock, etf] = train_data[stock].corr(train_data[etf])
    train_corr_matrix = train_data[stocks].corr()

    constraints = [{'type': 'eq', 'fun': lambda w: np.sum(w) - 1}]

    for i in range(loop_number):

        result = minimize(
            calc_sharpe_ratio,
            initial_weights,
            args=(cov_matrix_stocks_array, hist_returns_array, rf), 
            method='SLSQP',
            bounds= bounds,                           
            constraints = constraints
        )
        current_weights = pd.Series(result.x,  index=stocks)

        flagged_stocks = multi_risk_filter(train_corr_grid, train_corr_matrix, current_weights)
        
        if flagged_stocks.empty:
            return current_weights
        
        for _, row in flagged_stocks.iterrows():
            problem = row['correlated_stocks']
            total = current_weights[problem].sum()
            constraints.append({
            'type': 'ineq',
            'fun': lambda w, group=problem: combined_weight_thresh - pd.Series(w, index=stocks)[group].sum()
        })
    return current_weights



