# gen cov matrix function
def gen_cov_stocks(x):
    cov_matrix = np.cov(x, rowvar=False) * 252
    return cov_matrix


# gen Factor Analysis correlations between each stock and each etf --> keep for hidden concentration risk, will be utilized later
def factor_analysis_corr (returns_data):
    # new corr grid
    corr_grid = pd.DataFrame(index = stocks, columns= etfs, dtype=float)
    for stock in stocks:
        for etf in etfs:
            corr_grid.loc[stock, etf] = returns_data[stock].corr(returns_data[etf])
    return corr_grid

# Factor Analysis Beta values between each stock and each etf
def factor_analysis_beta(returns_data):
    beta_grid = pd.DataFrame(index=stocks, columns=etfs, dtype=float)
    for stock in stocks:
        for etf in etfs:
            cov = returns_data[stock].cov(returns_data[etf])
            var = returns_data[etf].var()
            beta_grid.loc[stock, etf] = cov / var
    return beta_grid

